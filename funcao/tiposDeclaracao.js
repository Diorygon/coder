// function declaration 
// O interpretador do JS interpreta antes da leitura da página
function soma(x,y){
    return x+y
}

// function expression
// só pode chamar a função depois de declarada
const sub = function (x,y){
    return x-y
}


// named function expression
// só pode chamar a função depois de declarada
// Não é muito utilizada...
const mult = function multi(x,y){
    return x*y
}


